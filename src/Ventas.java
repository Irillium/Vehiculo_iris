public class Ventas {
    private String codigoDeVenta;
    private String fechaDeLaVenta;
    private String cliente;
    private String vehículo;
    private String formaDePago;
    private String precioDeVenta;

    public String getCodigoDeVenta() {
        return codigoDeVenta;
    }

    public void setCodigoDeVenta(String codigoDeVenta) {
        this.codigoDeVenta = codigoDeVenta;
    }

    public String getFechaDeLaVenta() {
        return fechaDeLaVenta;
    }

    public void setFechaDeLaVenta(String fechaDeLaVenta) {
        this.fechaDeLaVenta = fechaDeLaVenta;
    }

    public String getCliente() {
        return cliente;
    }

    public void setCliente(String cliente) {
        this.cliente = cliente;
    }

    public String getVehículo() {
        return vehículo;
    }

    public void setVehículo(String vehículo) {
        this.vehículo = vehículo;
    }

    public String getFormaDePago() {
        return formaDePago;
    }

    public void setFormaDePago(String formaDePago) {
        this.formaDePago = formaDePago;
    }

    public String getPrecioDeVenta() {
        return precioDeVenta;
    }

    public void setPrecioDeVenta(String precioDeVenta) {
        this.precioDeVenta = precioDeVenta;
    }

}
